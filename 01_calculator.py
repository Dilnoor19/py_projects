a = int(input("Enter the first no. : "))
b = int(input("Enter the second no. : "))
x = input("Enter operation : ")

if x == "+":
    print("The Solution is : ", a + b)
if x == "-":
    print("The Solution is : ", a - b)
if x == "*":
    print("The Solution is : ", a * b)
if x == "/":
    print("The Solution is : ", a / b)
if x == "%":
    print("The Solution is : ", a % b)
if x == "//":
    print("The Solution is : ", a // b)
if x == "**":
    print("The Solution is : ", a ** b)